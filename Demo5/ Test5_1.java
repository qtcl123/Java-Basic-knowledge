public class Test5_1 
{
	public static void main(String[] args)
	{
		//生产汽车：在Java中通过new操作符来完成
		//其实就是在堆内存中产生一个实体
		//c是一个局部变量    处于主函数中	
		//变量存储于栈内存中
		Car c = new Car(); //c是一个类类型的变量   注意：类类型变量指向对象
		System.out.println(c.color);
		//将已有的车的颜色改成蓝色    在Java中的方式是：对象.对象成员！！！！！！！！！！~~~~~
		c.color = "blue";
		System.out.println(c.color);
		c.run();
		
		Car c1 = new Car();
		System.out.println(c1.color);
		
		
		System.out.println("-=-------------------");
		//例子：注意两对象指向同一个堆内存中的类     指向的地址一样
		Car c2 = new Car();
		c2.num = 5;
		Car c3 = c2;
		c3.color = "green";
		c3.run();
		
		
		
		
		
		
	}

}

