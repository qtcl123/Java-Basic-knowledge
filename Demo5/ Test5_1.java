//需求：描述汽车
//其实定义类：就是在描述事物   就是在定义属性和行为
//属性和行为：称为类中的成员（成员变量和成员方法【函数】）
class Car //类需要有一个名字
//事物的描述：属性和行为！！！！！！！！
    {
	//属性：颜色
	String color = "red";     //成员变量：作用于整个类中
	
	//字符串的在堆内存中：初始化值默认是null
	//属性：轮胎数
	int num = 4;
	
	//行为：运行
	void run()
	{
		System.out.println(color+"---"+num);
		
	}
	
	//一个类中们可以不使用主函数！！！！！！！！！！！！！
	
	}

